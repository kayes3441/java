package program;

public class Math_all {
	public static void main(String args[]) {
		System.out.println(Math.abs(-1000));
		System.out.println(Math.sqrt(5));
		System.out.println(Math.pow(6, 3));
		System.out.println(Math.log(8));
		System.out.println(Math.ceil(5.6));
		System.out.println(Math.floor(7.6));
		System.out.println(Math.round(10.99));
		System.out.println(Math.PI);
		
	}
}
